def batch_iter(train_data, train_label,batch_size):
    if not isinstance(train_label, torch.Tensor):
        train_label = torch.from_numpy(train_label.values)
    batch_num = train_data.shape[0]//batch_size
    idx = np.random.permutation(train_data.shape[0])
    for i in range(batch_num):
        lower_bound = i * batch_size
        upper_bound = min((i+1)*batch_size, train_data.shape[0])
        batch_idx = idx[lower_bound: upper_bound]
        batch_data = train_data[batch_idx, : ]
        if hasattr(batch_data, "toarray"):
            batch_data = torch.from_numpy(batch_data.toarray()).float()
        yield batch_data, train_label[batch_idx]

def softmax(x: torch.tensor) -> torch.tensor:
    """x: design matrix"""
    x_max = torch.max(x,axis = 1, keepdims = True).values
    x = x - x_max
    x_exp = torch.exp(x)
    partition = x_exp.sum(axis = 1, keepdims = True)
    return x_exp/partition

def cross_entropy_loss(y: torch.tensor, y_hat: torch.tensor)-> float:
    y = torch.argmax(y, axis = 1)
    e = -torch.log(y_hat[range(len(y_hat)), y] + 1e-7)
    return e.mean()

def mean_square_loss(y: torch.tensor, y_hat: torch.tensor)-> float:
    """带1/2的损失函数"""
    loss_per_sample = 0.5*torch.sum((y-y_hat)**2, dim =1)
    return torch.mean(loss_per_sample)

def calc_accuracy(y, y_hat):
    y = torch.argmax(y, axis = 1)
    y_hat = torch.argmax(y_hat, axis = 1)
    if y_hat.dtype != y.dtype:
        y_hat = y_hat.type(y.dtype)
    return torch.sum(y_hat==y)

class metrics_accumulator:
    def __init__(self, n):
        self.metrics = [0.0]*n

    def add(self,*args):
        self.metrics = [a + float(b) for a,b in zip(self.metrics, args)]

    def reset(self):
        self.metrics = [0.0] * len(self.metrics)

    def get_item(self, idx):
        return self.metrics[idx]

