class SoftmaxRegression:
    def __init__(self, W, b):

        self.W = W
        self.b = b

    def forward(self, batch):
        if not isinstance(batch, torch.Tensor):
            if hasattr(batch, "toarray"):
                batch = torch.from_numpy(batch.toarray()).float()
        return softmax(torch.matmul(batch, self.W) + self.b)

    def update(self, X, y_hat, y, lr: float, loss_type = 'ce', weight_decay = None):
        """
        loss_type: "ce" (Cross Entropy) 或 "mse"
        """
        if loss_type == 'ce':
          dZ = y_hat - y
        elif loss_type == 'mse':
          diff = y_hat - y
          term1 = y_hat*diff
          term2 = y_hat*torch.sum(term1,axis = 1,keepdims=True)
          dZ = term1 - term2
        else:
          raise ValueError("Unsupported loss type")
        m = X.shape[0]
        dW = torch.matmul(X.T, dZ) / m
        if weight_decay:
          dW += weight_decay*self.W
        db = torch.sum(dZ, axis=0) / m
        self.W = self.W - lr * dW
        self.b = self.b - lr * db

