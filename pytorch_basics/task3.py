def calc_exact_match(y_hat, y, pad_id):
    # y_hat: (B, Seq, Vocab), y: (B, Seq)
    pred_seq = torch.argmax(y_hat, dim=-1)
    # 忽略 padding 位置，非 padding 位置必须完全一致
    mask = (y != pad_id)
    # 预测正确 或 该位置是 padding (不纳入错误考核)
    correct_tokens = (pred_seq == y) | (~mask)
    # dim=-1 代表对整个序列的所有 token 求与(AND)操作
    exact_match = correct_tokens.all(dim=-1)
    return exact_match.sum().item(), y.size(0)

def build_model(config):
    embed_layer = build_embedding_layer(config)

    if config['model_type'] == "transformer":
        encoder_layer = TransformerEncoderLayer(config['model_dim'], config['num_heads'], config['dropout'], dim_feedforward=config['dim_feedforward'])
        encoder = TransformerEncoder(encoder_layer, config['num_encoders'])
        decoder_layer = TransformerDecoderLayer(config['model_dim'], config['num_heads'], config['dropout'], dim_feedforward=config['dim_feedforward'])
        decoder = TransformerDecoder(decoder_layer, config['num_decoders'])
        model = Transformer(encoder, decoder, embed_layer, config['model_dim'], config['vocab_size'], config['pad_id'], max_seq_len=config['max_seq_len'])
        model._init_weights()
        return model

    elif config['model_type'] == "decoder":
        layer = DecoderOnlyTransformerLayer(config['model_dim'], config['num_heads'], config['dropout'], dim_feedforward=config['dim_feedforward'])
        model = DecoderOnlyTransformer(layer, embed_layer, config['model_dim'], config['vocab_size'], config['pad_id'], config['layer_num'], max_seq_len=config.get('max_seq_len', 512))
        model._init_weights()
        return model
    else:
        raise ValueError("不支持的模型类型")

def build_optimizer(model, config):
  trainable_params = filter(lambda p: p.requires_grad, model.parameters())
  if config['optimizer'] == 'Adam':
      return torch.optim.Adam(trainable_params, lr=config['lr'])
  elif config['optimizer'] == 'Adadelta':
      return torch.optim.Adadelta(trainable_params, lr=config['lr'])
  elif config['optimizer'] == 'SGD':
      return torch.optim.SGD(trainable_params, lr=config['lr'], momentum=0.9)

class UniversalTrainer:
    def __init__(self, config, device, metrics_accumulator):
        self.config = config
        self.model = build_model(config).to(device)
        self.optimizer = build_optimizer(self.model, config)
        self.criterion = nn.CrossEntropyLoss(ignore_index=config['pad_id'])
        self.scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(self.optimizer, mode='min', factor=0.5, patience=3)

        self.device = device
        self.metrics = metrics_accumulator
        self.animator = Animator(task_type=config['task'])
        self.best_dev_loss = float('inf')
        self.patience_counter = 0
        self.history = []

    def train_and_evaluate(self, num_epochs, train_dataloader, dev_dataloader):
        global_step = 0
        observe_steps = [10, 100, 500, 1000, 3000, 5000, 10000]
        fixed_prompt = "Ross: Hey guys! "

        for epoch in range(num_epochs):
            self.model.train()
            self.metrics.reset()

            for batch in train_dataloader:
                global_step += 1 # 记录全局训练步数
                inputs, labels = batch
                inputs = {k: v.to(self.device) for k, v in inputs.items()}
                labels = labels.to(self.device)

                self.optimizer.zero_grad()
                outputs = self.model(**inputs)
                train_loss = self.criterion(outputs.view(-1, outputs.size(-1)), labels.view(-1))
                train_loss.backward()
                self.optimizer.step()

                if self.config['task'] == 'SubTask_1':
                    num_correct, batch_size = calc_exact_match(outputs, labels, self.config['pad_id'])
                    self.metrics.add(train_loss.item() * batch_size, num_correct, batch_size, 0, 0, 0)
                else:
                    valid_tokens = (labels != self.config['pad_id']).sum().item()
                    self.metrics.add(train_loss.item() * valid_tokens, 0, valid_tokens, 0, 0, 0)

                # 基于 Step 的文本生成观测钩子
                if self.config['task'] == 'SubTask_2' and global_step in observe_steps:
                    print(f"\n[{'='*15} Global Step: {global_step} (Epoch {epoch+1}) {'='*15}]")
                    # 临时切换到评估模式以关闭 Dropout
                    self.model.eval()
                    sample_text = generate_lm_text(self.model, self.config, prompt_text=fixed_prompt, max_new_tokens=100)
                    print(f"Prompt: {fixed_prompt}")
                    print(f"Output: {sample_text}\n")
                    # 观测完毕，切回训练模式继续学习
                    self.model.train()

            self.model.eval()
            with torch.no_grad():
                for batch in dev_dataloader:
                    dev_inputs, dev_labels = batch
                    dev_inputs = {k: v.to(self.device) for k, v in dev_inputs.items()}
                    dev_labels = dev_labels.to(self.device)

                    dev_outputs = self.model(**dev_inputs)
                    dev_loss = self.criterion(dev_outputs.view(-1, dev_outputs.size(-1)), dev_labels.view(-1))

                    if self.config['task'] == 'SubTask_1':
                        num_correct, batch_size = calc_exact_match(dev_outputs, dev_labels, self.config['pad_id'])
                        self.metrics.add(0, 0, 0, dev_loss.item() * batch_size, num_correct, batch_size)
                    else:
                        valid_tokens = (dev_labels != self.config['pad_id']).sum().item()
                        self.metrics.add(0, 0, 0, dev_loss.item() * valid_tokens, 0, valid_tokens)

            train_loss_avg = self.metrics.get_item(0) / self.metrics.get_item(2)
            dev_loss_avg = self.metrics.get_item(3) / self.metrics.get_item(5)

            if self.config['task'] == 'SubTask_1':
                train_metric = self.metrics.get_item(1) / self.metrics.get_item(2)
                dev_metric = self.metrics.get_item(4) / self.metrics.get_item(5)
            else:
                train_metric = math.exp(min(train_loss_avg, 20))
                dev_metric = math.exp(min(dev_loss_avg, 20))

            self.animator.add(epoch + 1, (train_loss_avg, train_metric, dev_loss_avg, dev_metric))
            self.scheduler.step(dev_loss_avg)

            metric_name = 'Exact_Match' if self.config['task'] == 'SubTask_1' else 'Perplexity'
            self.history.append({
                'Epoch': epoch + 1,
                'Train_Loss': train_loss_avg,
                f'Train_{metric_name}': train_metric,
                'Dev_Loss': dev_loss_avg,
                f'Dev_{metric_name}': dev_metric
            })

            if dev_loss_avg < self.best_dev_loss:
                self.best_dev_loss = dev_loss_avg
                best_state_dict = copy.deepcopy(self.model.state_dict())
                self.patience_counter = 0
            else:
                self.patience_counter += 1

            if self.patience_counter >= self.config['patience']:
                print(f"在第 {epoch+1} 轮触发早停")
                break

        pic_save_path = f"/content/drive/MyDrive/pytorch_beginner/task3/{self.config['exp_name']}_curve.pdf"
        self.animator.save_fig(pic_save_path)
        self.save_metrics_to_file()

        return self.best_dev_loss, best_state_dict

