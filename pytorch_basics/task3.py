class MultiheadAttention(nn.Module):
  def __init__(self, model_dim, num_heads, dropout = 0.1):
    super().__init__()
    assert model_dim % num_heads == 0, "d_model 必须能被 num_heads 整除"
    self.model_dim = model_dim
    self.head_dim = model_dim // num_heads
    self.num_heads = num_heads
    self.Wq = nn.Linear(model_dim, model_dim, bias=False)
    self.Wk = nn.Linear(model_dim, model_dim, bias=False)
    self.Wv = nn.Linear(model_dim, model_dim, bias=False)
    self.Wo = nn.Linear(model_dim, model_dim, bias=False)
    self.dropout = nn.Dropout(dropout)

  def forward(self, query, key, value, mask = None):
    #N: (batch_size, seq_len, model_dim)
    N = query.shape[0]
    #Q,K,V: (batch_size, num_heads, seq_len, head_dim)
    Q = self.Wq(query).view(N,-1,self.num_heads,self.head_dim).transpose(1,2)
    K = self.Wk(key).view(N,-1,self.num_heads,self.head_dim).transpose(1,2)
    V = self.Wv(value).view(N,-1,self.num_heads,self.head_dim).transpose(1,2)
    #scores: (batch_size, num_head, seq_len, seq_len)
    scores = torch.matmul(Q, K.transpose(-1,-2))/math.sqrt(self.head_dim)
    if mask is not None:
      #mask: (1,1,seq_len,seq_len)
      scores = scores.masked_fill(mask == 1, -1e8)
    #attention_weights: (batch_size, num_head, seq_len, seq_len)
    attention_weights = torch.softmax(scores, dim = -1)
    attention_weights = self.dropout(attention_weights)
    #context: (batch_size, num_heads, seq_len, head_dim)
    context = torch.matmul(attention_weights,V)
    context = context.transpose(1,2).contiguous().view(N,-1,self.model_dim)
    output = self.Wo(context)
    return output

class TransformerEncoderLayer(nn.Module):
  def __init__(self, model_dim, num_heads, dropout = 0.1, dim_feedforward = None):
    super().__init__()
    self.attention = MultiheadAttention(model_dim, num_heads, dropout)
    dim_feedforward = dim_feedforward or (model_dim * 4)
    self.FF = nn.Sequential(
        nn.Linear(model_dim,dim_feedforward),
        nn.ReLU(),
        nn.Linear(dim_feedforward,model_dim)
    )
    self.layer_norm1 = nn.LayerNorm(model_dim)
    self.layer_norm2 = nn.LayerNorm(model_dim)
    self.dropout = nn.Dropout(dropout)

  def forward(self,X,mask=None):
    attn_out = self.attention(X,X,X,mask=mask)
    o1 = self.layer_norm1(X + self.dropout(attn_out))
    ff_out = self.FF(o1)
    o2 = self.layer_norm2(o1 + self.dropout(ff_out))
    return o2

class TransformerEncoder(nn.Module):
  def __init__(self, encoder_layer, num_encoders = 6):
    super().__init__()
    self.layers = nn.ModuleList([copy.deepcopy(encoder_layer) for _ in range(num_encoders)])
    model_dim = encoder_layer.layer_norm1.normalized_shape[0]
    self.norm = nn.LayerNorm(model_dim)

  def forward(self, X, mask = None):
    output = X
    for layer in self.layers:
      output = layer(output,mask)
    output = self.norm(output)
    return output

class TransformerDecoderLayer(nn.Module):
  def __init__(self, model_dim, num_heads, dropout = 0.1, dim_feedforward = None):
    super().__init__()
    self.attention1 = MultiheadAttention(model_dim, num_heads, dropout)
    self.attention2 = MultiheadAttention(model_dim, num_heads, dropout)
    dim_feedforward = dim_feedforward or (model_dim * 4)
    self.FF = nn.Sequential(
        nn.Linear(model_dim,dim_feedforward),
        nn.ReLU(),
        nn.Linear(dim_feedforward,model_dim)
    )
    self.layer_norm1 = nn.LayerNorm(model_dim)
    self.layer_norm2 = nn.LayerNorm(model_dim)
    self.layer_norm3 = nn.LayerNorm(model_dim)
    self.dropout = nn.Dropout(dropout)

  def forward(self,tgt,Encoder_output,tgt_mask=None,memory_mask=None):
    attn_out1 = self.attention1(tgt,tgt,tgt,mask=tgt_mask)
    o1 = self.layer_norm1(tgt + self.dropout(attn_out1))
    attn_out2 = self.attention2(o1,Encoder_output,Encoder_output,mask=memory_mask)
    o2 = self.layer_norm2(o1 + self.dropout(attn_out2))
    ff_out = self.FF(o2)
    o3 = self.layer_norm3(o2 + self.dropout(ff_out))
    return o3

class TransformerDecoder(nn.Module):
  def __init__(self, decoder_layer, num_decoders = 6):
    super().__init__()
    self.layers = nn.ModuleList([copy.deepcopy(decoder_layer) for _ in range(num_decoders)])
    model_dim = decoder_layer.layer_norm1.normalized_shape[0]
    self.norm = nn.LayerNorm(model_dim)

  def forward(self, tgt, Encoder_output,tgt_mask=None,memory_mask=None):
    output = tgt
    for layer in self.layers:
      output = layer(output, Encoder_output, tgt_mask=tgt_mask, memory_mask=memory_mask)
    output = self.norm(output)
    return output

class Transformer(nn.Module):
  def __init__(self, TransformerEncoder, TransformerDecoder, embed_layer, model_dim, vocab_size, pad_id, max_seq_len=50):
      super().__init__()
      self.pad_id = pad_id
      self.embedding = embed_layer
      self.pos_embedding = nn.Embedding(max_seq_len, model_dim)
      self.encoder = TransformerEncoder
      self.decoder = TransformerDecoder
      self.linear = nn.Linear(model_dim,vocab_size)

  def forward(self, src, tgt_input):
    N, seq_len_x = src.shape
    _, seq_len_tgt = tgt_input.shape
    device = src.device
    # (N, seq_len_x) 扩充为 (N, 1, 1, seq_len_x)
    encoder_mask = (src == self.pad_id).unsqueeze(1).unsqueeze(2)

    causal_mask = torch.triu(torch.ones(seq_len_tgt, seq_len_tgt, dtype=torch.bool, device=device), diagonal=1)
    causal_mask = causal_mask.unsqueeze(0).unsqueeze(0)

    tgt_pad_mask = (tgt_input == self.pad_id).unsqueeze(1).unsqueeze(2) # (N, 1, 1, seq_len_tgt)

    tgt_mask = causal_mask | tgt_pad_mask

    memory_mask = encoder_mask

    position_x = torch.arange(0, seq_len_x, device=device).unsqueeze(0).expand(N, seq_len_x)
    position_tgt = torch.arange(0, seq_len_tgt, device=device).unsqueeze(0).expand(N, seq_len_tgt)

    X_emb = self.embedding(src) + self.pos_embedding(position_x)
    tgt_emb = self.embedding(tgt_input) + self.pos_embedding(position_tgt)

    encoder_output = self.encoder(X_emb, mask=encoder_mask)
    decoder_output = self.decoder(tgt_emb, encoder_output, tgt_mask=tgt_mask, memory_mask=memory_mask)
    output = self.linear(decoder_output)

    return output

  @torch.no_grad()
  def generate(self, src, bgn_id, max_len):
    self.eval()
    batch_size = src.size(0)
    current_input = torch.full((batch_size, 1), bgn_id, dtype=torch.long, device=src.device)
    for _ in range(max_len):
      prediction = self.forward(src=src,tgt_input = current_input)
      next_token = torch.argmax(prediction[:,-1,:],dim=-1,keepdim=True)
      current_input = torch.cat([current_input,next_token],dim=1)
    return current_input

  def _init_weights(self):
    for p in self.parameters():
      if p.dim()>1:
        nn.init.xavier_uniform_(p)

class DecoderOnlyTransformerLayer(nn.Module):
  def __init__(self,model_dim, num_heads, dropout = 0.1, dim_feedforward = None):
    super().__init__()
    self.attention = MultiheadAttention(model_dim, num_heads, dropout)
    dim_feedforward = dim_feedforward or (model_dim*4)
    self.FF = nn.Sequential(
        nn.Linear(model_dim, dim_feedforward),
        nn.ReLU(),
        nn.Linear(dim_feedforward, model_dim)
    )
    self.layer_norm1 = nn.LayerNorm(model_dim)
    self.layer_norm2 = nn.LayerNorm(model_dim)
    self.dropout = nn.Dropout(dropout)

  def forward(self,data,mask = None):
    attn_o1 = self.attention(data,data,data,mask = mask)
    o1 = self.layer_norm1(data + self.dropout(attn_o1))
    FF_output = self.FF(o1)
    o2 = self.layer_norm2(o1 + self.dropout(FF_output))
    return o2

class DecoderOnlyTransformer(nn.Module):
    def __init__(self, DecoderOnlyTransformerLayer, embed_layer, model_dim, vocab_size, pad_id, layer_num=2, max_seq_len=512):
        super().__init__()
        self.pad_id = pad_id
        self.embedding = embed_layer
        self.pos_embedding = nn.Embedding(max_seq_len, model_dim)
        self.decoders = nn.ModuleList([copy.deepcopy(DecoderOnlyTransformerLayer) for _ in range(layer_num)])
        self.linear = nn.Linear(model_dim, vocab_size)

    def forward(self, x, mask=None):
        N, seq_len = x.shape
        device = x.device
        causal_mask = torch.triu(torch.ones(seq_len, seq_len, dtype=torch.bool, device=device), diagonal=1)
        causal_mask = causal_mask.unsqueeze(0).unsqueeze(0)
        pad_mask = (x == self.pad_id).unsqueeze(1).unsqueeze(2)
        mask = causal_mask | pad_mask

        pos_encoding = torch.arange(0, seq_len, device=device).unsqueeze(0).expand(N, seq_len)
        output = self.embedding(x) + self.pos_embedding(pos_encoding)
        for decoder in self.decoders:
            output = decoder(output, mask=mask)
        return self.linear(output)

    @torch.no_grad()
    def generate(self,x,max_len):
        self.eval()
        current_input = x
        for _ in range(max_len):
            # 动态截取，防止自回归生成的长度超过位置编码支持的最大长度
            max_seq = self.pos_embedding.num_embeddings
            context = current_input[:, -max_seq:] if current_input.size(1) > max_seq else current_input
            prediction = self.forward(context)
            next_token = torch.argmax(prediction[:, -1, :], dim=-1, keepdim=True)
            current_input = torch.cat([current_input, next_token], dim=1)
        return current_input

    def _init_weights(self):
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

class metrics_accumulator:
    def __init__(self, n):
        self.metrics = [0.0]*n

    def add(self,*args):
        self.metrics = [a + float(b) for a,b in zip(self.metrics, args)]

    def reset(self):
        self.metrics = [0.0] * len(self.metrics)

    def get_item(self, idx):
        return self.metrics[idx]

